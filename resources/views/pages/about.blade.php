<h1>About Us</h1>

<p>
    The Philippine Society of Information Technology Educators Foundation, Inc. (PSITE) is an organization that aims to promote quality Information Technology Education in the country through workshops, seminars, industry-academe, bench-marking activities, and programs designed to benefit its members and the academic sector in general. PSITE Foundation was granted the certificate of incorporation by the Securities and Exchange Commission (SEC Reg. No. A199901393) on March 31, 1999.
</p>
<p>
    The Society shall serve as a venue for the optimization and development of faculty members, curricula, projects, research, resources, linkages, and assistance in policy formulation related to IT education. Since its inception, the organization has been led by competent and dynamic IT academe/industry professionals
</p>
<p>
    PSITE – Central Visayas, formerly PSITE-7, was formally organized for it’s members coming from the four islands of Central Visayas namely, Cebu, Bohol, Negros Oriental and Siquijor.
</p>

